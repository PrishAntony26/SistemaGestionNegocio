    public void matriz(){
        //Se mostrara las cantidad total de cada tipo de dispositivos en cada empresa y tambien las ganancias totales 
        ArrayList<HistorialVenta> hV = new ArrayList<HistorialVenta>();
        m.insertarEmpresasInventario();
        ArrayList<Empresa> e = m.getListaE();
        String m[][] = new String[3][8];
        //Recorremos a todos las empresas para saber cuales fueron sus ventas, esto con la ayuda del metodo cliente
        for(int i=0;i<e.size();i++){
           hV = c.rescatarHistorialVenta1(e.get(i));
           int aux1=0,aux2=0,aux3=0,aux4=0,aux5=0;
           double totalGanancias = 0;
           //hacemos esta sentencia if ya que si no se ha realizado ninguna accion de venta por parte de esa empresa todos los valores seran 0 
           if(hV!=null){  
             for(int j=0;j<hV.size();j++){
                 if(hV.get(j).getDispositivo().equals("Placa madre")){
                     aux1 = aux1 + hV.get(j).getCantidadVendida();
                 }
                 if(hV.get(j).getDispositivo().equals("Disco duro ")){
                     aux2 = aux2 + hV.get(j).getCantidadVendida();
                 }
                 if(hV.get(j).getDispositivo().equals("Memoria Ram")){
                     aux3 = aux3 + hV.get(j).getCantidadVendida();
                 }
                 if(hV.get(j).getDispositivo().equals("Procesador")){
                     aux4 = aux4 + hV.get(j).getCantidadVendida();
                 }
                 if(hV.get(j).getDispositivo().equals("Tarjeta de video")){
                     aux5 = aux5 + hV.get(j).getCantidadVendida();;
                 }
                 totalGanancias= totalGanancias + hV.get(j).getTotal();
                 System.out.println("xd");
               }
               m[i][0] = e.get(i).getNombre();
               m[i][1] = e.get(i).getRUC();
               m[i][2] = String.valueOf(aux1);
               m[i][3] = String.valueOf(aux2);
               m[i][4] = String.valueOf(aux3);
               m[i][5] = String.valueOf(aux4);
               m[i][6] = String.valueOf(aux5);
               m[i][7] = String.format("%.2f",totalGanancias);
           }else{
               m[i][0] = e.get(i).getNombre();
               m[i][1] = e.get(i).getRUC();
               m[i][2] = String.valueOf(0);
               m[i][3] = String.valueOf(0);
               m[i][4] = String.valueOf(0);
               m[i][5] = String.valueOf(0);
               m[i][6] = String.valueOf(0);
               m[i][7] = String.valueOf(0);
           }
        }
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
            m,
            new String [] {
                "Nombre", "RUC", "Placa madre", "Disco duro", "Memoria Ram", "Procesador", "Tarjeta de video", "Total de ganancias (S/.)"
            }
        ));
    }
