    public void ConstrucciondeTablaPorDefecto(){
        tabla = new DefaultTableModel();
        tabla.addColumn("Codigo");
        tabla.addColumn("Marca");
        tabla.addColumn("Tipo");
        tabla.addColumn("Capacidad");
        tabla.addColumn("Empresa");
        tabla.addColumn("Stock");
        tabla.addColumn("Precio Unitario (S/.)");
        this.TablaMRCotizados.setModel(tabla);
    }
    
    public void DatosEnTabla(){
        for(int i=0;i<Memoria.size();i++){
           String[] informacion= new String[7];
           informacion[0] = String.valueOf(Memoria.get(i).getCodigo());;
           informacion[1] = Memoria.get(i).getMarca();
           informacion[2] = Memoria.get(i).getTipo();
           informacion[3] = String.valueOf(Memoria.get(i).getCapacidad());
           informacion[4] = Memoria.get(i).getnEmpresa();
           informacion[5] = String.valueOf(Memoria.get(i).getStockEmpresa());
           informacion[6] = String.valueOf(Memoria.get(i).getPrecio());
           tabla.addRow(informacion);
        }
    }
    
