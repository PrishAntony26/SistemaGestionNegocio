    public void matriz(){
        //En esta matriz ir√° cada cliente que se haya registrado y haya realizado una compra 
        ArrayList<Registrado> r = new ArrayList<Registrado>();
        r = c.rescatarRegistrados();
        String m[][] = new String[r.size()][5];
        //Recorremos a todos los usuarios registrados
        for(int i=0;i<r.size();i++){
            double totalGastado = 0;
            ArrayList<FacturaEmpresa> F = new ArrayList<FacturaEmpresa>();
            F = r.get(i).LanzarFacturas();
            //Con esto estamos dandole al usuario sus facturas por las compras que ha realizado para asi poder cuanto en total ha gastado por todas
            // las compras que ha realizado 
            if(F!=null){
                for(int j=0;j<F.size();j++){
                    double aux3[] = F.get(j).getPrecioUnitario();
                    int aux4[] = F.get(j).getCantidad();
                    totalGastado = totalGastado + aux3[0]*aux4[0];
                }
                m[i][0] = r.get(i).getNombre();
                m[i][1] = String.valueOf(r.get(i).getDNI());
                m[i][2] = r.get(i).getDireccion();
                m[i][3] = String.valueOf(r.get(i).getTelefono());
                m[i][4] = String.valueOf(totalGastado);
            }else{
                
            }
        }
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
            m,
            new String [] {
                "Nombre", "DNI", "Direccion", "Telefono", "Total gastado (S/.)"
            }
        ));
    }
